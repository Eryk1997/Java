public class Employee {
    private String name;
    private String surname;
    private int age;

    public void setName(String n)
    {
        name=n;
    }
    public void setSurname(String s)
    {
        surname=s;
    }
    public void setAge(int age)
    {
        this.age=age;
    }
    public String getName()
    {
        return this.name;
    }
    public String getSurname()
    {
        return surname;
    }
    public int getAge()
    {
        return age;
    }



}
